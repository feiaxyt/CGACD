def smooth_l1_loss(bbox_pred, bbox_targets, sigma=3.0):
    sigma_2 = sigma ** 2
    in_box_diff = bbox_pred - bbox_targets
    abs_in_box_diff = torch.abs(in_box_diff)
    smoothL1_sign = (abs_in_box_diff < 1. / sigma_2).detach().float()
    out_loss_box = torch.pow(in_box_diff, 2) * (sigma_2 / 2.) * smoothL1_sign \
        + (abs_in_box_diff - (0.5 / sigma_2)) * (1. - smoothL1_sign)
    loss_box = out_loss_box.sum() / out_loss_box.shape[0]
    return loss_box

def l1_loss(bbox_pred, bbox_targets):
    loss = (bbox_pred - bbox_targets).abs()
    return loss.sum().div(bbox_pred.shape[0])

def crop_hwc(image, bbox, out_sz, padding=(0, 0, 0)):
    bbox = [float(x) for x in bbox]
    a = (out_sz-1) / (bbox[2]-bbox[0])
    b = (out_sz-1) / (bbox[3]-bbox[1])
    c = -a * bbox[0]
    d = -b * bbox[1]
    mapping = np.array([[a, 0, c],
                        [0, b, d]]).astype(np.float)
    crop = cv2.warpAffine(image, mapping, (out_sz, out_sz), borderMode=cv2.BORDER_CONSTANT, borderValue=padding)
    return crop

